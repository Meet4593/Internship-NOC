/**
 * Sidebar Component
 * Left navigation sidebar with logo and navigation items
 * Responsive design that collapses on mobile
 */

import { useRouter } from 'next/router'
import Link from 'next/link'

const navigationItems = [
  { name: 'Dashboard', href: '/dashboard', icon: DashboardIcon },
  { name: 'My Applications', href: '/applications', icon: ApplicationsIcon },
  { name: 'Company Profile', href: '/company-profile', icon: CompanyIcon },
  { name: 'New Application', href: '/new-application', icon: NewApplicationIcon },
  { name: 'Profile', href: '/profile', icon: ProfileIcon },
]

